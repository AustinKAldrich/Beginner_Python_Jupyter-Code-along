def area_calculator(l, w):
    """
        area_calculator(length, width)

        Returns the area of a rectangle by multiplying a given length and width.
    """
    return l*w

def circle_circum(r):
    """
    circle_circum(radius)

    Returns the circumference of a circle given a radius.
    """
    from math import pi
    return 2 * pi * r